import * as fs from 'fs';
import * as path from 'path';
import { info, userPath, warn } from '../kernel';

export async function checkLarvaFile(): Promise<void> {
  if (!(await getLarvaFile())) {
    info('Error: this command must be runned on a Larva.js project repository (generated by "larva create app").');
    process.exit();
  }
}

export async function getLarvaFile(): Promise<string> {
  return await getFile(path.resolve(userPath(), './larva'));
}

export async function getPackageFile(): Promise<any> {
  const packageFile = await getFile(path.resolve(__dirname, '../../package.json'));

  if (packageFile) {
    return JSON.parse(packageFile);
  } else {
    warn("package.json file doesn't exists !");
  }
}

export async function getFile(filePath: string): Promise<string> {
  return new Promise((resolve, reject) => {
    fs.readFile(filePath, 'utf8', (err: NodeJS.ErrnoException, file: string) => {
      if (err) {
        reject();
      }
      resolve(file);
    });
  })
    .then((packageFile: string) => packageFile)
    .catch(() => null);
}
